   <p style="text-align: justify; font-size: 7pt; "><strong>CONTRATO DE COMODATO </strong>QUE CELEBRAN POR UNA PARTE EL COLEGIO DE BACHILLERES DEL ESTADO DE CAMPECHE, A QUIEN EN LO SUCESIVO SE LE DENOMINARÁ <strong>“EL COMODANTE”, </strong>REPRESENTADA POR LA C. ING. ADLEMI SANTIAGO RAMÍREZ M. en M, , EN SU CARÁCTER DE DIRECTORA GENERAL, Y POR LA OTRA PARTE EL (LA) C. <strong>{{responsable}}, </strong>A QUIEN EN LO SUCESIVO SE LE DENOMINARÁ <strong>“EL COMODATARIO”</strong>; Y A LA TOTALIDAD DE SUSCRIPTORES EN SU CONJUNTO SE LES DENOMINARÁ <strong>“LAS PARTES”, </strong>SUJETANDOSE AL TENOR DE LAS DECLARACIONES Y CLÁUSULAS QUE A CONTINUACIÓN SE DESCRIBEN:</p>



    <p style="text-align: center;font-size: 7pt; "><strong>D E C L A R A C I O N E S</strong></p>

    <p style="text-align: left;font-size: 7pt; "><strong>I.-</strong> De <strong>“EL COMODANTE”,</strong> a través de su representante, que:</p>
    
    <p style="text-align: justify;font-size: 7pt; margin: 3px 0;"><strong>I.I.- </strong>Que de acuerdo con el artículo 1 de la Ley que Crea el Colegio de Bachilleres del Estado de Campeche, el Colegio de Bachilleres del Estado de Campeche es un organismo público descentralizado, con personalidad jurídica y patrimonio propio, integrado al sector paraestatal.</p>

    <p style="text-align: justify;font-size: 7pt; margin: 3px 0;"><strong>I.II.- </strong>Que se encuentra representado por la Ing. Adlemi Santiago Ramírez M. en M. en su carácter de Directora General de conformidad a lo estipulado en las fracciones I, III y X del artículo 32 del texto vigente de la Ley que Crea el Colegio de Bachilleres del Estado de Campeche, personalidad que acredita mediante nombramiento expedido por el Lic. Rafael Alejandro Moreno Cárdenas Gobernador Constitucional del Estado de Campeche, con fecha 16 de septiembre de 2015, de conformidad con lo estipulado en los artículos 71 fracción VI de la constitución política del Estado de Campeche, 9 de la Ley Orgánica de la Administración Pública del Estado de Campeche, 21 de la Ley de la Administración Pública Paraestatal del Estado de Campeche y 29 de la Ley que Crea el Colegio de Bachilleres del Estado de Campeche</p>

    <p style="text-align: justify;font-size: 7pt; margin: 3px 0;"><strong>I.III.- </strong>Que el servicio de educación media superior que ofrece, lo lleva a cabo a través de Centros Educativos, tales como el <strong>{{plantel}}</strong>.</p>

    <p style="text-align: justify;font-size: 7pt; margin: 3px 0;"><strong>I.IV.- </strong>Que “EL COMODANTE” cuenta con equipos de cómputo y la capacidad legal de entregarlos en comodato a los alumnos adscritos al <strong>{{plantel}}, </strong>, para uso exlusivo de su formación académica y desarrollo educativo.</p>

    <p style="text-align: justify;font-size: 7pt; margin: 3px 0;"><strong>I.V.- </strong>Para los efectos y fines legales del presente contrato señala el domicilio legal el inmueble ubicado en calle Oliver, número 14, entre calle Lorenzo Alfaro Alomía y Avenida Miguel Alemán área Ah Kim Pech, código postal 24014, en la ciudad de San Francisco de Campeche, municipio de Campeche, estado de Campeche, con registro federal de contribuyentes CBC900824F44.</p>

     <p style="text-align: left;font-size: 7pt; margin: 3px 0;"><strong>II.-</strong> De <strong>“EL COMODATARIO,</strong> por su propio y personal derecho y en representación de “EL ALUMNO”, que:</p>

     <p style="text-align: justify;font-size: 7pt; margin: 3px 0;"><strong>II.I.- El (La) C. {{responsable}}, </strong>declara ser mexicano (a) por nacimiento, mayor de edad legal, con domicilio en <strong>{{direccion}}, </strong>de la localidad del plantel y que cuenta con capacidad jurídica para contratar, sin que exista ninguna circunstancia que pudiera invalidar o afectar el objeto del cumplimiento del presente contrato</p>



     <p style="text-align: justify;font-size: 7pt; margin: 3px 0;"><strong>II.II.- </strong>Que el bien recibido en comodato estará destinado a favor de <strong>“EL ALUMNO”, </strong>a quien representa en este Contrato.</p>


     <p style="text-align: left;font-size: 7pt; margin: 3px 0;"><strong>III.-</strong> De <strong>“LAS PARTES”, </strong> que:</p>

     <p style="text-align: justify;font-size: 7pt; margin: 3px 0;"><strong>III.I-</strong> Que se reconocen la personalidad jurídica con la que comparecen y la capacidad legal que ostentan para asumir derechos y obligaciones establecidas en el presente instrumento.</p>

     <p style="text-align: justify;font-size: 7pt; margin: 3px 0;"><strong>III.I-</strong> Que se reconocen la personalidad jurídica con la que comparecen y la capacidad legal que ostentan para asumir derechos y obligaciones establecidas en el presente instrumento.</p>

     <p style="text-align: justify;font-size: 7pt; margin: 3px 0;"><strong>III.II-</strong> Que conocen el valor y alcance legal del presente instrumento, en el cual no existen vicios en el consentimiento que pudieren invalidarlo.</p>

     <p style="text-align: left;font-size: 7pt; margin: 3px 0;">En mérito de las declaraciones, <strong>“LAS PARTES”, </strong>acuerdan las siguientes:</p>

    <p style="text-align: center;font-size: 7pt;"><strong>C  L  Á  U  S  U  L  A  S</strong></p>



    <p style="text-align: justify;font-size: 7pt; margin:3px 0;"><strong>PRIMERA.- “LAS PARTES”</strong> convienen en celebrar contrato de comodato de computadora, tipo Laptop; para ello <strong>“EL COMODANTE”</strong>se obliga a conceder gratuitamente a <strong>“EL COMODATARIO” </strong>el uso temporal de un equipo de cómputo laptop de la marca HP Chromebook 11 G5 EE , número de serie <strong>{{serial}}</strong></p>

    <p style="text-align: justify;font-size: 7pt; margin: 3px 0;"><strong>SEGUNDA.- “EL COMODATARIO”</strong> acepta recibir de <strong>“EL COMODANTE”</strong> la posesión del bien material de este contrato, mismo que se encuentra condicionado para el uso exclusivo de <strong>“EL ALUMNO”,</strong> con las aplicaciones y programas que tiene a la fecha de entrega, comprometiéndose a no permitir que este último inserte ninguna aplicación, programa o cualquier otra herramienta, diferente a las que recibe.</p>

    <p style="text-align: justify;font-size: 7pt; margin: 3px 0;"><strong>TERCERA.- “EL COMODANTE” </strong> otorga a <strong>“EL COMODATARIO”</strong> el bien objeto del presente Contrato para el uso exclusivo de <strong>“EL ALUMNO”,</strong> de su enseñanza educativa que contribuya a su formación y desarrollo educativo.</p>

    <p style="text-align: justify;font-size: 7pt; margin: 3px 0;"><strong>CUARTA.- “El COMODATARIO”</strong> se compromete a que <strong>“EL ALUMNO</strong> participe en las actividades de capacitación para el uso del equipo de cómputo y la implementación del uso de la tecnología en la enseñanza.</p>

    <p style="text-align: justify;font-size: 7pt; margin: 3px 0;"><strong>QUINTA.- “EL COMODATARIO”</strong> se compromete y obliga a vigilar que <strong>“EL ALUMNO</strong> realice el buen uso del equipo y lo mantenga en óptimas condiciones.</p>

    <p style="text-align: justify;font-size: 7pt; margin: 3px 0;"><strong>SEXTA.- “EL COMODANTE” </strong> se reserva el derecho de supervisar y vigilar, en cualquier tiempo, que el bien sea usando conforme a los términos y condiciones previstas en este contrato, y <strong>“EL COMODATARIO”</strong> se obliga a otorgarles todas las facilidades para que se lleven a cabo las actividades de supervisión y vigilancia.</p>

 
